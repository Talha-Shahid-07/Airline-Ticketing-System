    public class UserDatabase {

        public ArrayList<String[]> getUser() {
            ArrayList<String[]> UserList = new ArrayList<>();
            ConnectionToDB conn = new ConnectionToDB();
            Connection con = conn.EstablishConnection();
            String query = "SELECT * FROM user"; // Adjust the query based on your database schema

            try {
                Statement stmt = con.createStatement();
                ResultSet rs = stmt.executeQuery(query);

                while (rs.next()) {
                    String[] user = new String[3]; // Adjust the size based on the number of columns
                    user[0] = rs.getString("userName");
                    user[1] = rs.getString("email");
                    user[2] = rs.getString("phoneNumber");

                    UserList.add(user);
                }
            } catch (SQLException e) {
                e.printStackTrace();
            }

            return UserList;
        }
    }

    public class UserTableModel extends DefaultTableModel {

        private final String[] columnNames = {"User Name", "Email", "Phone Number"};

        public UserTableModel(ArrayList<String[]> userData) {
            super();
            this.setColumnIdentifiers(columnNames);
            for (String[] user : userData) {
                this.addRow(user);
            }
        }

        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // Make cells non-editable
        }
    }

    private void displayUser() {
        UserDatabase userDatabase = new UserDatabase();
        ArrayList<String[]> user = userDatabase.getUser();
        UserTableModel model = new UserTableModel(user);
        jTable1.setModel(model);
    }

