    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        String username = txtUsername.getText();
        if (username.isEmpty()) {
            // Handle empty ID case
            javax.swing.JOptionPane.showMessageDialog(this, "Please enter an Username.");
            return;
        }

        ConnectionToDB conn = new ConnectionToDB();
        Connection con = conn.EstablishConnection();
        String query = "DELETE FROM user WHERE userName = \'" + username + "\'";

        try {
            Statement stmt = con.createStatement();
            int rowsAffected = stmt.executeUpdate(query);
            if (rowsAffected > 0) {
                javax.swing.JOptionPane.showMessageDialog(this, "User deleted successfully.");
                // Refresh the table
                displayUser();
            } else {
                javax.swing.JOptionPane.showMessageDialog(this, "User not found.");
            }
        } catch (SQLException e) {
            e.printStackTrace();
            javax.swing.JOptionPane.showMessageDialog(this, "Error deleting User. Enter a valid Username!");
        } finally {
            try {
                con.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

