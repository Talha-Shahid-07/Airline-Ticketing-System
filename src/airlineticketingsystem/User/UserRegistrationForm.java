    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        String name = txtName.getText();
        String email = txtEmail.getText();
        String password = String.valueOf(pwdUser.getPassword());
        String phoneNumber = txtPhNum.getText();

        if (!name.matches("[a-zA-Z]+")) {
            JOptionPane.showMessageDialog(this, "Invalid name! Please enter only alphabets for your name.");
            return;
        }

        if (!email.contains("@")) {
            JOptionPane.showMessageDialog(this, "Invalid email! Please enter a valid email address containing '@'.");
            return;
        }

        UserRegisterClass register = new UserRegisterClass(this); // Pass the form reference
        register.addUser(name, email, password, phoneNumber);
    }//GEN-LAST:event_btnRegisterActionPerformed


