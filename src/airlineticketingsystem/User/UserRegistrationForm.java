    class UserRegisterClass {

        ConnectionToDB conn = new ConnectionToDB();
        Connection con = conn.EstablishConnection();
        Statement stmt = null;
        UserRegistrationForm form;

        public UserRegisterClass(UserRegistrationForm form) {
            this.form = form;
        }

        public void addUser(String name, String email, String password, String phoneNumber) {
            String registerQuery = "insert into User(UserName,Email,Password,PhoneNumber) values ('" + name + "','" + email + "','" + password + "','" + phoneNumber + "')";
            try {
                stmt = con.createStatement();
                int res = stmt.executeUpdate(registerQuery);
                if (res < 0) {
                    JOptionPane.showMessageDialog(null, "Error!");
                } else {
                    JOptionPane.showMessageDialog(null, "Registered!");
                    UserLoginForm loginForm = new UserLoginForm();
                    loginForm.setVisible(true);
                    form.setVisible(false); // Hide the registration form
                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }

        }

    }

